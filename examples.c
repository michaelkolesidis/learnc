/* Circle length and circumference */ 

#include <stdio.h>
#include <stdlib.h>
#define PI 3.141592

int main()
{ 
    float r, circum, area;
    printf("Enter circle radius:\n");
    scanf("%f", &r);
    circum = 2 * PI * r;
    area = PI * r * r;

    printf("Circumference = %f\nArea = %f", circum, area);
    return 0;
}

